function changeLanguage(lang) {
  document.querySelectorAll("[data-translate]").forEach(element => {
    let key = element.getAttribute("data-translate");
    if (translations[lang][key]) {
      element.innerHTML = translations[lang][key];
    }
  });

  // Dịch placeholder trong input và textarea
  document.querySelectorAll("[data-translate-placeholder]").forEach(element => {
    let key = element.getAttribute("data-translate-placeholder");
    if (translations[lang][key]) {
      element.setAttribute("placeholder", translations[lang][key]);
    }
  });
  // Lưu trạng thái ngôn ngữ vào localStorage
  localStorage.setItem("selectedLanguage", lang);
}

// Kiểm tra và áp dụng ngôn ngữ được lưu trước đó
document.addEventListener("DOMContentLoaded", () => {
  let savedLang = localStorage.getItem("selectedLanguage") || "vi";
  changeLanguage(savedLang);

  // Gán sự kiện click cho các nút chuyển đổi ngôn ngữ
  document.querySelector(".languages .vi").addEventListener("click", () => changeLanguage("vi"));
  document.querySelector(".languages .en").addEventListener("click", () => changeLanguage("en"));
});
